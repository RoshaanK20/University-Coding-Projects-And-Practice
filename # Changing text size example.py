# Changing text size example
print("\033[1;31;40mThis is large text\033[0m")  # 1 is for bold, 31 is for red text, 40 is for black background
print("\033[2;31;40mThis is normal text\033[0m")  # 2 is for normal intensity
print("\033[3;31;40mThis is small text\033[0m")   # 3 is for faint (small) text
